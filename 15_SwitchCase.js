// Switch case example: Similar to if-else, compares expression values

var value = 2; // Define the value to be checked

switch (value) {
    case 1:
        console.log(1);
        break;
    case 2:
        console.log(2);
        break;
    case 3:
        console.log(3);
        break;
    default:
        console.log("Default case");
        break;
}
